#!/bin/bash

# mostrar somente data e hora 
date

# mostrar apenas o digito da hora
date +%H

# mostrar apenas os digitos do minuto
date +%M

# mostrar o mês (m minusculo)
date +%m

# mostrar apenas a data (mês, dia e ano)
date +%D

# mostrar o dia apenas (d minusculo)
date +%d

# mostrar dia e mês
date +%m/d

